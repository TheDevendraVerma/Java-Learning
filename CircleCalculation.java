          public class CircleCalculation {
		    public static void main(String[] args) {
		        // Radius define kiya
		        double radius = 5.0;

		        // Formula lagaya (PI = 3.14159)
		        double area = Math.PI * radius * radius;
		        double circumference = 2 * Math.PI * radius;

		        // Result print karwaya
		        System.out.println("Circle ki Radius: " + radius + " cm");
		        System.out.println("Circle ka Area: " + area + " sq cm");
		        System.out.println("Circle ki Circumference: " + circumference + " cm");
		    }
		}
	


